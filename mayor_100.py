#Entrada:
# Aquí recogemos los datos que el usuario ingresa por teclado.
#Proceso:
#Aquí recogemos los datos que el usuario ingresa por teclado.
#Salida:
#Se imprime el resultado y se sabe si el número es mayor o menor que 100

